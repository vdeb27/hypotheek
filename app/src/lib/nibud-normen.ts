/**
 * Zoek het financieringslastpercentage op basis van toetsinkomen en rentepercentage.
 * Rondt inkomen af naar de dichtstbijzijnde €1.000 en zoekt de bijbehorende rij op.
 * Inkomen onder €30k of boven €125k wordt afgekapt op de tabelgrenzen.
 */
export function zoekFinancieringslastpercentage(toetsinkomen: number, rentePercentage: number): number {
  // Bepaal de juiste rentekolom
  let renteIdx = RENTE_GRENZEN.findIndex((grens) => rentePercentage <= grens);
  if (renteIdx === -1) renteIdx = RENTE_GRENZEN.length - 1; // >= 6.501%

  // Rond af naar dichtstbijzijnde €1.000 en clamp op tabelgrenzen
  const afgerond = Math.round(toetsinkomen / 1000) * 1000;
  const clamped = Math.max(TABEL[0][0], Math.min(TABEL[TABEL.length - 1][0], afgerond));

  // Zoek de rij (tabel loopt per €1.000 vanaf €30.000)
  const rijIdx = (clamped - TABEL[0][0]) / 1000;

  return TABEL[rijIdx][renteIdx + 1];
}
